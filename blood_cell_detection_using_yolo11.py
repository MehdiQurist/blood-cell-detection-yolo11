def convert_json_to_yolo(json_path, save_path):
    """DatasetNinja JSON → YOLO TXT dönüştürücü"""

    with open(json_path, "r") as f:
        data = json.load(f)

    img_w = data["size"]["width"]
    img_h = data["size"]["height"]

    with open(save_path, "w") as out:
        for obj in data["objects"]:

            cls = obj["classTitle"]
            if cls not in classes:
                continue

            cls_id = classes.index(cls)

            xmin = obj["points"]["exterior"][0][0]
            ymin = obj["points"]["exterior"][0][1]
            xmax = obj["points"]["exterior"][1][0]
            ymax = obj["points"]["exterior"][1][1]

            # YOLO normalize format
            x = (xmin + xmax) / 2 / img_w
            y = (ymin + ymax) / 2 / img_h
            w = (xmax - xmin) / img_w
            h = (ymax - ymin) / img_h

            out.write(f"{cls_id} {x} {y} {w} {h}\n")

