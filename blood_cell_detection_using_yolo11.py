import shutil

def process_split(split):
    img_dir = f"BCCD/{split}/img"
    ann_dir = f"BCCD/{split}/ann"

    out_img = f"dataset/images/{split}"
    out_lab = f"dataset/labels/{split}"

    count = 0

    for ann_file in os.listdir(ann_dir):
        if not ann_file.endswith(".json"):
            continue

        ann_path = os.path.join(ann_dir, ann_file)
        img_name = ann_file.replace(".json", "")
        img_path = os.path.join(img_dir, img_name)

        # Etiket oluştur
        label_save_path = os.path.join(out_lab, ann_file.replace(".json", ".txt"))
        convert_json_to_yolo(ann_path, label_save_path)

        # Görüntüyü kopyala
        shutil.copy(img_path, os.path.join(out_img, img_name))

        count += 1

    print(f"{split.upper()} set oluşturuldu ✔ {count} adet örnek")


