empty_files = []
wrong_format_files = []
correct_files = []

for file in os.listdir(label_path):
    if file.endswith(".txt"):
        full = os.path.join(label_path, file)
        content = open(full, "r").read().strip()

        # Dosya boş mu?
        if len(content) == 0:
            empty_files.append(file)
            continue

        # Satırları kontrol et
        valid = True
        for line in content.splitlines():
            parts = line.split()
            if len(parts) != 5:
                valid = False
                break
            try:
                cls = int(parts[0])
                nums = list(map(float, parts[1:]))
                if not all(0 <= n <= 1 for n in nums):
                    valid = False
                    break
            except:
                valid = False
                break

        if valid:
            correct_files.append(file)
        else:
            wrong_format_files.append(file)

